#Arithmatic functions

def addition(x,y):
    return x + y

def subtraction(x,y):
    return x - y

def multiplication(x,y):
    return x * y

def division(x,y):
    return x / y

def exp(x,y):
    return x**y

def inv(x,y):
    return x**(-y)

def conc(x,y):
    return x + y

def add_ex(x):
    return x + '!'